CREATE TABLE km (Datum VARCHAR(10) NOT NULL,
    typ VARCHAR(40) NOT NULL,
    kennzeichen VARCHAR(10) NOT NULL,
    km VARCHAR(8) NOT NULL,
    bemerkung VARCHAR(50) NOT NULL,
    von VARCHAR(8) NOT NULL,
    bis VARCHAR(8) NOT NULL,
    km_monat VARCHAR(10),
    km_saison VARCHAR(10) NOT NULL
); 
